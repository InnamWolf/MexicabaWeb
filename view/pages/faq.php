    <!-- FAQ-01 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq1" role="button" aria-expanded="false" aria-controls="faq1">
      <span>¿Quién puede contratar un plan de ahorro educativo en Mexicana de Becas?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq1">

      <div class="card card-body response">
        El contratante puede crear un plan de ahorro educativo para sus hijos, nietos y sobrinos. Incluso, en México la
        figura de “padrino” también puede contratar en Mexicana de Becas. El niño o niña debe estar cursando 3º grado de
        primaria para plan de ahorro educativo para preparatoria, y en el caso de desear contratar el plan de
        universidad, el niño o niña debe estar cursando como máximo 4º grado de primaria.
      </div>
      
    </div>

    <!-- FAQ-02 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq2" role="button" aria-expanded="false" aria-controls="faq2">
      <span>¿Cuánto es lo mínimo que puedo ahorrar?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq2">

      <div class="card card-body response">
      No existe un mínimo de ahorro pues se trata de planes de ahorro totalmente personalizados de acuerdo con tus finanzas. Armamos un plan de ahorro educativo
      a tu medida en donde tú decides el monto y la periodicidad de tu ahorro objetivo, en función de tus posibilidades económicas actuales: pago único, anual, semestral,
      trimestral o mensual.
      </div>
      
    </div>

    <!-- FAQ-03 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq3" role="button" aria-expanded="false" aria-controls="faq3">
      <span> ¿Qué edad deben tener mis hijos para poder contratar un plan de ahorro educativo con Mexicana de Becas?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq3">

      <div class="card card-body response">
       Mientras más pronto comiences es mejor. Lo ideal es empezar cuando ellos estén recién nacidos o son pequeños (entre 0 y 5 años), ya que los gastos que implican
        no son tan grandes como los que se van a presentar conforme vayan creciendo. Además, debes considerar que la inflación educativa incrementa año con año.
        <br> <br>
        Para poder contratar un plan para Preparatoria, el niño o niña debe estar cursando 2º grado de primaria, y en el caso de desear contratar el plan de universidad, el niño
        o niña debe estar cursando como máximo 4º grado de primaria.
      </div>
      
    </div>

    <!-- FAQ-04 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq4" role="button" aria-expanded="false" aria-controls="faq4">
      <span> ¿Qué pasa si mi hijo decide no estudiar?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq4">

      <div class="card card-body response">
      Si llegado el momento de elegir carrera, el beneficiario se encuentra dudoso sobre qué estudiar, pueden posponer su ingreso a la universidad un año; y mientras tanto,
      su dinero invertido seguirá generando rendimientos durante ese periodo de gracia.
      <br> <br>
      Si a pesar de esto, tu hijo(a) decide no estudiar, se puede optar por la opción de nombrar a otro beneficiario; siempre y cuando, este nuevo beneficiario sea menor
      que el beneficiario original.
      <br> <br>
      El peor escenario es cancelar el plan, ya que como tiene el beneficio fiscal, si no se ocupa finalmente para educación, existe una penalización sobre el rendimiento.

      </div>
      
    </div>

    <!-- FAQ-05 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq5" role="button" aria-expanded="false" aria-controls="faq5">
      <span> ¿Qué pasa si mi hijo estudia en una universidad pública o en alguna universidad que no esté dentro de la red de convenios de Mexicana de Becas?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq5">

      <div class="card card-body response">
        El beneficiario puede estudiar en la universidad que desee, pública o privada, nacional o extranjera, siempre y cuando tenga validez oficial. Hay muchas familias
        que ahorraron con nosotros pensando en que probablemente tendrían que cubrir colegiaturas costosas y que él o la joven se encuentran estudiando en la UNAM,
        IPN, UAM u otra institución pública.
        <br> <br>
        La red de convenios con instituciones privadas de prestigio tiene el objetivo de acercar descuentos con los cuales se pueden ahorrar hasta un año de colegiatura,
        pero hay absoluta libertad de estudiar en la universidad o preparatoria, y carrera que desee; siempre y cuando estos cuenten con reconocimiento de validez oficial
        en México o incluso en el extranjero, y sean legalmente reconocidas.
        <br> <br>
        En Mexicana de Becas no vendemos escuelas. Aunque te presentamos algunas opciones para ayudarte a realizar una proyección de tu ahorro, tú y tu hijo(a) tienen
        absoluta libertad de elegir la universidad o preparatoria de su preferencia; siempre y cuando tenga validez y reconocimiento oficial.

      </div>
      
    </div>

