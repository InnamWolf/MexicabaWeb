    <!-- FAQ-07 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq7" role="button" aria-expanded="false" aria-controls="faq7">
      <span> ¿Qué es el gasto de administración?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq7">

      <div class="card card-body response">
        Este gasto además de ser el costo por la gestión de tus recursos a lo largo de la vida de tu plan de ahorro, te da acceso a múltiples beneficios adicionales durante el
        tiempo en que tu inversión esté con nosotros. Se cobra únicamente durante las primeras a portaciones** de tu plan de ahorro.
        <br> <br>
        <span class="faq__txt--Small">** Pueden ser de 12 a 14 aportaciones mensuales o su equivalente de acuerdo con la periodicidad de aportación y tipo de Plan. </span>
      </div>
      
    </div>

    <!-- FAQ-08 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq8" role="button" aria-expanded="false" aria-controls="faq8">
      <span> ¿Para qué sirve el seguro de pago saldado?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq8">

      <div class="card card-body response">
        Este seguro protege las aportaciones remanentes* de tu plan de ahorro, con la finalidad de asegurar la educación de tu beneficiario en el caso de que cualquiera
        de los contratantes se viera limitado para continuar realizando sus aportaciones en caso de fallecimiento o invalidez total o permanente.
        <br> <br>
        <span class="faq__txt--Small">** Que faltan por cubrir. </span>
      </div>
      
    </div>

    <!-- FAQ-09 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq9" role="button" aria-expanded="false" aria-controls="faq9">
      <span>  ¿Puedo volver a contratar un plan al finalizar uno?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq9">

      <div class="card card-body response">
        Si, siempre y cuando el beneficiario se encuentre cursando como máximo 4° año de primaria para Plan de universidad y 2° año primaria para Plan de preparatoria.
      </div>
      
    </div>

    <!-- FAQ-10 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq10" role="button" aria-expanded="false" aria-controls="faq10">
      <span>  ¿Qué pasa si estoy atravesando por un momento económico difícil y no puedo continuar aportando a mi plan de ahorro contratado?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq10">

      <div class="card card-body response">
        En este caso, el contratante titular puede comunicarse con Mexicana de Becas para indicarnos que está atravesando un momento económico difícil y buscar alternativas
        para sobrellevar esta situación, que le permitan continuar con el plan de ahorro y evitar llegar a la cancelación, que es lo menos recomendable.
        <br><br>
        Es importante señalar que dichas alternativas tienen que ver con la postergación de las aportaciones planeadas, por lo cual la estimación que se tenía en un inicio de
        cuánto monto iba a generar en el futuro, definitivamente va a variar, ya que durante un periodo se estaría interrumpiendo el ahorro.
      </div>
      
    </div>

    <!-- FAQ-11 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq11" role="button" aria-expanded="false" aria-controls="faq11">
      <span> ¿Qué pasa si cancelo?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq11">

      <div class="card card-body response">
        De manera general, el tipo de penalización va a depender del momento en el que se hace la cancelación, y estas serán sobre los rendimientos generados. Al existir
        el beneficio fiscal y no cumplirse el objetivo final que es la educación del beneficiario, existirá una penalización sobre el rendimiento, la cual se expresa en el contrato.
      </div>
      
    </div>

    <!-- FAQ-12 -->

    <a class="faq__question" data-bs-toggle="collapse" href="#faq12" role="button" aria-expanded="false" aria-controls="faq12">
      <span> ¿Puedo incrementar la suma que contraté al principio?</span> <i class="fa-solid fa-chevron-down"></i>
    </a>

    <div class="collapse faq__resp" id="faq12">

      <div class="card card-body response">
        
        Si el contrato se encuentra cerrado o durante el periodo de aportaciones y el beneficiario se encuentra en posibilidad económica para adquirir un nuevo contrato,
        puede incrementar su inversión a través de la contratación de un nuevo contrato.
        <br><br>
        Una vez que haya concluido con sus aportaciones, pero el beneficiario se encuentra fuera del rango de edad para adquirir un nuevo contrato y no ha llegado el Año de
        Rentabilidad*, puede habilitar la promoción de Ahorra un año más, con la cual puede seguir aportando directo a su capital durante 1 año adicional.el contrato.
        <br><br>
        <span class="faq__txt--Small">*Año de Rentabilidad: año en el cual inicia la fase de reembolso, esto de acuerdo con el contrato.</span>

      </div>
      
